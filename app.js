const state = {
  lang: localStorage.getItem('bnk_lang') || 'ru'
};

const numberFormat = new Intl.NumberFormat('ru-RU');

function formatNumber(value) {
  return numberFormat.format(Math.round(Number(value) || 0));
}

function setRangeProgress(input) {
  const min = Number(input.min || 0);
  const max = Number(input.max || 100);
  const value = Number(input.value || 0);
  const progress = ((value - min) / (max - min)) * 100;
  input.style.setProperty('--range-progress', `${Math.max(0, Math.min(100, progress))}%`);
}

function applyTranslations(lang) {
  const dict = translations[lang] || translations.ru;
  document.documentElement.lang = lang === 'kg' ? 'ky' : 'ru';
  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.dataset.i18n;
    if (dict[key]) node.textContent = dict[key];
  });
  document.querySelectorAll('[data-i18n-html]').forEach((node) => {
    const key = node.dataset.i18nHtml;
    if (dict[key]) node.innerHTML = dict[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (dict[key]) node.setAttribute('placeholder', dict[key]);
  });
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  localStorage.setItem('bnk_lang', lang);
  state.lang = lang;
}

function initNavigation() {
  const burger = document.getElementById('burger');
  const nav = document.getElementById('nav');
  const links = nav ? nav.querySelectorAll('a') : [];
  if (!burger || !nav) return;

  function closeMenu() {
    nav.classList.remove('is-open');
    burger.classList.remove('is-open');
    burger.setAttribute('aria-expanded', 'false');
    document.body.classList.remove('no-scroll');
  }

  burger.addEventListener('click', () => {
    const open = nav.classList.toggle('is-open');
    burger.classList.toggle('is-open', open);
    burger.setAttribute('aria-expanded', String(open));
    document.body.classList.toggle('no-scroll', open);
  });

  links.forEach((link) => link.addEventListener('click', closeMenu));
  document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') closeMenu();
  });
}

function initReveal() {
  const items = document.querySelectorAll('.reveal');
  if (!items.length) return;
  if (!('IntersectionObserver' in window)) {
    items.forEach((item) => item.classList.add('visible'));
    return;
  }
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.14 });
  items.forEach((item) => observer.observe(item));
}

function initCalculator() {
  const amountRange = document.getElementById('amountRange');
  const termRange = document.getElementById('termRange');
  const calcType = document.getElementById('calcType');
  const amountOut = document.getElementById('amountOut');
  const termOut = document.getElementById('termOut');
  const paymentOut = document.getElementById('monthlyPayment');
  const rateOut = document.getElementById('rateOut');
  const totalOut = document.getElementById('totalOut');
  if (!amountRange || !termRange || !calcType) return;

  const rates = {
    mortgage: 0.16,
    auto: 0.19,
    business: 0.22
  };

  function calculate() {
    setRangeProgress(amountRange);
    setRangeProgress(termRange);
    const amount = Number(amountRange.value);
    const term = Number(termRange.value);
    const annualRate = rates[calcType.value] || rates.mortgage;
    const monthlyRate = annualRate / 12;
    const payment = amount * (monthlyRate * Math.pow(1 + monthlyRate, term)) / (Math.pow(1 + monthlyRate, term) - 1);
    amountOut.textContent = formatNumber(amount);
    termOut.textContent = String(term);
    paymentOut.textContent = formatNumber(payment);
    rateOut.textContent = `${Math.round(annualRate * 100)}%`;
    totalOut.textContent = formatNumber(payment * term);
  }

  [amountRange, termRange, calcType].forEach((el) => el.addEventListener('input', calculate));
  calculate();
}

function cleanPhone(value) {
  return value.replace(/\D/g, '').replace(/^996/, '').slice(0, 9);
}

function formatKgPhone(value) {
  const digits = cleanPhone(value);
  const a = digits.slice(0, 3);
  const b = digits.slice(3, 5);
  const c = digits.slice(5, 7);
  const d = digits.slice(7, 9);
  let result = '+996';
  if (a) result += ` (${a}`;
  if (a.length === 3) result += ')';
  if (b) result += ` ${b}`;
  if (c) result += `-${c}`;
  if (d) result += `-${d}`;
  return result;
}

function showFieldError(field, message) {
  const wrapper = field.closest('.field');
  const error = wrapper ? wrapper.querySelector('.error') : null;
  if (wrapper) wrapper.classList.toggle('invalid', Boolean(message));
  if (error) error.textContent = message || '';
}

